// Helper function to get all villages (kelurahan + desa) for a kecamatan
export function getVillagesForKecamatan(kecamatanName: string): string[] {
  const location = locations.find(loc => 
    loc.kecamatan.toLowerCase() === kecamatanName.toLowerCase()
  )
  if (!location) return []
  
  // Combine kelurahan and desa
  return [...location.kelurahan, ...location.desa]
}

// Helper function to format kecamatan list for display
export function formatKecamatanList(): string {
  return locations.map((loc, index) => 
    `${index + 1}. ${loc.kecamatan}`
  ).join('\n')
}

// Helper function to format village list for a kecamatan
export function formatVillageList(kecamatanName: string): string {
  const villages = getVillagesForKecamatan(kecamatanName)
  if (villages.length === 0) return ''
  
  return villages.map((village, index) => 
    `${index + 1}. ${village}`
  ).join('\n')
}

// Helper function to get kecamatan by number
export function getKecamatanByNumber(num: number): LocationData | null {
  const index = num - 1
  if (index < 0 || index >= locations.length) return null
  return locations[index]
}

// Helper function to get village by number for a kecamatan
export function getVillageByNumber(kecamatanName: string, num: number): string | null {
  const villages = getVillagesForKecamatan(kecamatanName)
  const index = num - 1
  if (index < 0 || index >= villages.length) return null
  return villages[index]
}

